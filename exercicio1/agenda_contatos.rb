# Agenda de Contatos
# Neste exercício, vamos criar uma agenda de contatos simples em Ruby. O programa permite adicionar, listar e buscar contatos, mas não armazena dados após a execução. Vamos explorar conceitos como hashes, strings e estruturas de controle. Utilizaremos um loop para manter o menu ativo e expressões regulares para tratar as entradas. Ao final, teremos uma aplicação funcional que gerencia contatos temporariamente, praticando a manipulação de dados e a interação com o usuário. Vamos lá!

# Exercício
# Um programa para gerenciar uma lista de contatos (nome e telefone), salvos enquanto o programa está em execução.

# Descrição
# O usuário pode escolher opções em um menu para adicionar um novo contato, listar todos os contatos salvos ou buscar por um contato específico.

# Conceitos Praticados
# Fundamentos: Entrada e saída básica.
# Tipos de Dados:
# Hashes: Estrutura ideal para guardar os contatos (ex: {"Fulano" => "99999-8888"}).
# Arrays: Array de hashes para a lista mais complexa.
# Strings e Símbolos: Para nomes e chaves do hash.
# Estruturas de Controle:
# Um laço principal para manter o menu ativo.
# case para tratar as opções do menu (adicionar, listar, sair).

