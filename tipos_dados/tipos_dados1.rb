# Racionais, Booleanos e nulos

# Racionais (Floats)
pi = 3.14
puts pi.class # => Float

n1 = 3 / 4
n2 = 1.0 / 3
resultado = n1 + n2
puts resultado

# Booleanos (TrueClass e FalseClass)
verdadeiro = true
falso = false
puts verdadeiro.class # => TrueClass
puts falso.class # => FalseClass

# Nulos (NilClass)
nulo = nil
puts nulo.class # => NilClass